public class Human {
    private int age;
    private float weight, height;
    private String name;
    private boolean male;

    public int getAge() {
        return age;
    }

    public float getWeight() {
        return weight;
    }

    public float getHeight() {
        return height;
    }

    public String getName() {
        return name;
    }

    public boolean isMale() {
        return male;
    }
}
